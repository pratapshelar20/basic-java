package Array;

public class demoArray {

	public static void main(String[] args) {
		int abc[]=new int[4];
		abc[0]=10;
		abc[1]=20;
		abc[2]=30;
		abc[3]=40;
		System.out.println(abc[0]);//10
		System.out.println(abc[1]);//20
		System.out.println(abc[2]);//30
		System.out.println(abc[3]);//40
		System.out.println(abc[0]+abc[1]);
		System.out.println(abc[0]-abc[1]);
		System.out.println(abc[0]*abc[1]);
		System.out.println(abc[0]/abc[1]);
		System.out.println(abc[0]>abc[1]);
		System.out.println(abc[0]<abc[1]);
		System.out.println(abc[0]==abc[1]);
		System.out.println(abc[0]!=abc[1]);
		 }

	}


